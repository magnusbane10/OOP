public class Persoana {
    private int varsta;
    private String nume;
    private String CNP;
    private Adresa adresa;


    Persoana() {

    }

    Persoana(String nume, int varsta, String CNP, Adresa adresa) {
        this.nume = nume;
        this.varsta = varsta;
        this.CNP = CNP;
        this.adresa = adresa;
    }

    public boolean  getNrOreLucratoare(){
        return getNrOreLucratoare();
    }

    public int getVarsta() {
        return varsta;
    }

    public void setVarsta(int varsta) {
        this.varsta = varsta;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getCNP() {
        return CNP;
    }

    public void setCNP(String CNP) {
        this.CNP = CNP;
    }

    public Adresa getAdresa() {
        return adresa;
    }

    public void setAdresa(Adresa adresa) {

        this.adresa = adresa;
    }

public int getNrOreSaptamanalLucrate(){
        return getNrOreSaptamanalLucrate();
}

}


